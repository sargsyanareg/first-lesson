package lesson.two;

public class PrimitiveTypeExample {
    public static void main(String[] args) {
        int boxOne = 12;
        int boxTwo = 6;
        int sum = boxOne + boxTwo;
        int division = boxOne / boxTwo;
        int multiplication = boxOne * boxTwo;
        System.out.println(multiplication);
        System.out.println("առաջին + երկրորդ = " + sum);
        System.out.println("առաջին / երկրորդ = " + division);
    }
}
